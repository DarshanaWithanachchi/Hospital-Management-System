    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed

        String nicno = txtnicno.getText();


        Search_Patient ss = new Search_Patient();
        PatientDetails dd = Search_Patient.sechPatientDetails(nicno);

        txtfirstname1.setText(dd.getFirstname());
        txtfirstname1.setEditable(false);              //can't update 
        txtlastname1.setText(dd.getLastname());
        txtlastname1.setEditable(false);                //can't update 
        txtaddress1.setText(dd.getAddress());
        txtage1.setText(dd.getAge() + "");
        cmbsex1.setSelectedItem(dd.getSex());
        cmbsex1.setSelectedItem(false);                 //can't update 
        txtcontactno1.setText(dd.getContactno() + "");
        txtadmitdate1.setText(dd.getAdmitdate() + "");
        txtrecomanddoctor1.setText(dd.getRecomanddoctor());
        txtconfirmdoctor1.setText(dd.getConfirmdoctor());

    }//GEN-LAST:event_searchActionPerformed

