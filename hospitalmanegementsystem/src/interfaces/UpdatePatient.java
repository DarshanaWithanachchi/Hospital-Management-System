    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed

        PatientDetails pd = new PatientDetails();

        pd.setAddress(txtaddress1.getText());
        pd.setAge(Integer.parseInt(txtage1.getText()));
        pd.setContactno(Integer.parseInt(txtcontactno1.getText()));
        pd.setAdmitdate(Integer.parseInt(txtadmitdate1.getText()));
        pd.setRecomanddoctor(txtrecomanddoctor1.getText());
        pd.setConfirmdoctor(txtconfirmdoctor1.getText());

        Update_Patient up = new Update_Patient();
        boolean asd = up.updatePayDetails(pd, txtnicno.getText());

        if (asd) {
            JOptionPane.showMessageDialog(this, "UPDATE SUCCESS"); //say update success
        }


    }//GEN-LAST:event_updateActionPerformed

